export function setLocale(locale: Locale) {
  localStorage.setItem("xllmapi_locale", locale);
}

export function t(key: string, locale?: Locale): string {
  const l = locale ?? getLocale();
  return messages[l]?.[key] ?? messages.en?.[key] ?? key;
}
